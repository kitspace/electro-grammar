const assert = require('better-assert')

const {parse} = require('../lib/index')

describe('parsing', () => {
  it("doesn't parse nonsense", () => {
    const c = parse('this is total rubbish')
    assert(c.type == null)
  })
  it('returns empty object on empty', () => {
    assert(parse('').type == null)
  })
})

